function splitIntoRows<T>(items: T[], rowCount: number): T[][] {
    const rows = Array.from({ length: rowCount }, () => [] as T[]);
    items.forEach((item, index) => {
        rows[index % rowCount].push(item);
    });
    return rows;
}

